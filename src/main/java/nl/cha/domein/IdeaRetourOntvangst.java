package nl.cha.domein;

public class IdeaRetourOntvangst {
	
	long nrdeclaratie;
	int uzovi;
	String factuurnummer;
	
	
	public long getNrdeclaratie() {
		return nrdeclaratie;
	}
	public void setNrdeclaratie(long nrdeclaratie) {
		this.nrdeclaratie = nrdeclaratie;
	}
	public int getUzovi() {
		return uzovi;
	}
	public void setUzovi(int uzovi) {
		this.uzovi = uzovi;
	}
	public String getFactuurnummer() {
		return factuurnummer;
	}
	public void setFactuurnummer(String factuurnummer) {
		this.factuurnummer = factuurnummer;
	}

}
